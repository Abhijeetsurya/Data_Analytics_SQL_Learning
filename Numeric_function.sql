# Numeric Function


# Round, ceil, floor, abs, mod, log, power

# Round
SELECT ROUND(25.78) AS Rounded_num;

# Ceil

SELECT CEIL(25.36) AS ceil;

SELECT CEIL(-25.36) AS ceil;

# Floor

SELECT FLOOR(22.23) AS floor_;

SELECT FLOOR(-22.23) AS floor_;

# Power

SELECT POWER(3, 2) As Square_of_3;


# ABS 

SELECT ABS(-23);


# LOG

SELECT LOG(10);

SELECT LOG2(10);











